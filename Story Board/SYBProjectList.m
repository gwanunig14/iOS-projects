#pragma mark - Table view data source

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [[SYBData mainData].allProjects count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell"];
    
    NSDictionary * projects = [SYBData mainData].allProjects[indexPath.row];
    
    UILabel * projectName = [[UILabel alloc]initWithFrame:CGRectMake(2, 2, self.view.frame.size.width -12, 20)];
    projectName.text = projects[@"title"];
    
    if (cell == nil) cell = [[UITableViewCell alloc]initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"cell"];
    
    [cell addSubview:projectName];
    
    return cell;
}

